    private void cbTipoTelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoTelActionPerformed
        if (cbTipoTel.getSelectedIndex() != -1) {
            txtTelefone.setEnabled(true);
            try {
                txtTelefone.setFormatterFactory(null);
                if (TipoTelefoneEnum.valueOf(cbTipoTel.getSelectedItem().toString()) == TipoTelefoneEnum.Celular)
                    txtTelefone.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("(##)#####-####")));
                else
                    txtTelefone.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("(##)####-####")));
            } catch (ParseException ex) {
                Messages.showError("Erro de m√°scara: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_cbTipoTelActionPerformed

    private void preencherTipos() {
        model = new DefaultComboBoxModel<>();
        for (TipoTelefoneEnum tipo : TipoTelefoneEnum.values()) {
            model.addElement(tipo);
        }
        cbTipoTel.setModel(model);
        cbTipoTel.setSelectedIndex(-1);
    }

